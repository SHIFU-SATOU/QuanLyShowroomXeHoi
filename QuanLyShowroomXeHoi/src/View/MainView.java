    void showXeTable(Connection KetNoi) {

        ShowroomController temp = new ShowroomController();

        try {
            ResultSet rs = temp.loadXe(KetNoi);
            while (rs.next()) {
                this.XeTableModel.addRow(new Object[]{rs.getString("MSXE"),
                    rs.getString("HANG"), rs.getString("DONG"),
                    rs.getString("GIA"), rs.getInt("SOLUONG")});
            }
        } catch (SQLException ex) {
            err.println("Không thể tải dữ liệu xe từ cơ sở dữ liệu!");
        }
    }

